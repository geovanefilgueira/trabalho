package pojo;

/**
 *
 * @author cawil
 */
public class Usuario {
    private int id;
    private String nome;
    private String status;
    private int nada;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the nada
     */
    public int getNada() {
        return nada;
    }

    /**
     * @param nada the nada to set
     */
    public void setNada(int nada) {
        this.nada = nada;
    }

    
    
}
